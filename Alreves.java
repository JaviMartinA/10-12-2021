package com.primero;

public class Alreves {

	public static void main(String[] args) {
		String cadena = "Buenos dias Carmelo";
		String invertida = "";
		for (int i = cadena.length() - 1; i >= 0; i--) {
			invertida += cadena.charAt(i);
			}
			System.out.println("Cadena invertida: " + invertida);

			}
		

	}
